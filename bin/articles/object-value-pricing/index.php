				<h1><b>Object Value Pricing</b></h1>
				<h2>(Originally posted at <a href="http://the-pastry-box-project.net/dan-mall/2012-may-5/">The Pastry Box Project</a>)</h2>
			</hgroup>
			
			<p>Few people talk about pricing in our industry, and for good reason. There&rsquo;s hardly a straight answer for &ldquo;What should I charge?&rdquo; It depends on many factors: the value of the project to your client, the amount of inquiries you currently have, the type of project, who the client is, the services needed, the amount of time necessary to complete the project, the amount of time you have available, and many more.</p>

			<p>For even the greenest of designers, gut reaction is often disregarded for logic, to their own detriment. Trust your gut; it can tell you many things that a spreadsheet cannot.</p>

			<p>Here&rsquo;s a system I use to help qualify my gut reaction. Ask yourself what object you would barter for this project. Let&rsquo;s choose a website redesign as an example, a type of project many likely get. Choose an object to represent the value of your project. If your client offered you a new iPod touch instead of cash, would that be fair to you? Probably (hopefully) not, which means you value this project at a higher rate than $199 (currently the going price for an iPod touch). What about a Playstation 3? An iPad 2? Dinner at your choice of 3-star Michelin restaurants? A Canon EOS 5D Mark III?  A week-long all expense paid trip to the resort of your choice? Rent for a year? A new Volkswagen Touareg?</p>

			<p>Let&rsquo;s say you thought the Canon EOS 5D Mark III was a fair trade for this website design. Since the current price for that camera is about $3500, that gives you a good sense of the minimum you would charge for this project. While I certainly don&rsquo;t suggest making this the only way that you evaluate value, it can definitely help you to hone in that gut reaction to get a sense of where you really place the value of your project.</p>

			<p>If you&rsquo;re interested in learning more about how to price a project, check out these excellent resources:</p>
	
