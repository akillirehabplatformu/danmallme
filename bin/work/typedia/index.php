            <div class="main">

                <p>My final project in college was a web-based Flash app called &ldquo;TypeCompanion&rdquo; that loaded all the active fonts on your computer into an artboard. Think Adobe Illustrator, but on the web. Though merely a proof of concept, I was already keenly aware of the shortage of resources available for browsing, finding, and choosing type. At the same time, my friend <a href="http://jasonsantamaria.com/">Jason</a> was thinking about the same problem.</p> 

                <p>In 2006, we decided to do something about it. We started creating a wiki-like site that allowed for a user-generated database of typefaces. Roping in the best of the best as volunteers to architect the site, classify fonts, and build a sustainable and scalable content-management system, we finished the site 3 years later. Enjoy <a href="http://typedia.com/">Typedia</a>, the fruit of our labor!</p>

