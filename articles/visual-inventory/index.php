					<p>We asked questions like, "Could <strong>case studies</strong> be the driving metaphor for the site?" and "Is Sifter&rsquo;s brand strong enough to support a <strong>monochromatic</strong> approach?" </p>

					<p>Garrett, being the smart and thoughtful guy he was, gave great feedback like&hellip;</p>

					<ul>
						<li>&ldquo;I&rsquo;m not afraid to do bold, but I do believe that our marketing should also communicate our focus on the &rsquo;get-out-of-the-way&rsquo; style interface that we&rsquo;re moving towards&rdquo; </li>
						<li>&ldquo;With the type-driven and color-constrained, I feel like that&rsquo;s taking it too far&rdquo;</li>
						<li>&ldquo; The color palette and style [from the <abbr title="American Airlines">AA</abbr> example] really jumped out at me. I feel like the details are a bit noisy and distracting, but the large solid colors/textures felt right.&rdquo;</li>
						<li>&hellip;and lots of other good stuff</li>
					</ul>

				</section><!-- .chunk -->			

				<section id="sifter-actions" class="chunk50 light70">

					<p>One day and two Basecamp comments later, we had a conceptual direction and a visual range to explore. That kind of actionable output has previously taken me three weeks and <a href="http://cognition.happycog.com/article/the-magic-number" title="&ldquo;The Magic Number,&rdquo; by Happy Cog&rsquo;s Chris Cashdollar">three concepts</a> to get to, two of which would have been thrown out. I&rsquo;ll still do multiple concepts now, but they&rsquo;ll more likely be iterations and variations of each other that actually encourage &ldquo;frankensteining&rdquo; rather than being wildly different and independent. </p>

					<p>Of course, getting valuable&mdash;and, more importantly, actionable output from this kind of process is dependent upon both parties trusting each other enough to have honest communication. I&rsquo;m sure some naysayers will say that some clients don&rsquo;t have the capacity to understand this kind of abstraction, but that&rsquo;s a problem of <a href="/articles/prequalifying-clients/">choosing the right clients</a>, not one of proper deliverables. </p>

				</section><!-- .chunk -->	

