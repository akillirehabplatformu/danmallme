	    <section class="clearfix wrap vanilla">			
			
			<p>My apprentices and I were reviewing their work schedules when it struck me: are they putting in enough time? How much time is &ldquo;enough time?&rdquo;</p>

            <p>Like most questions I don&rsquo;t know the answer to, I <a href="https://twitter.com/danielmall/statuses/636893941028290560">asked my friends</a>: </p>

            <blockquote class="twitter-tweet" lang="en"><p lang="en" dir="ltr">How long is the optimal work day? Why? Any good reading or research on the topic?</p>&mdash; Dan Mall (@danielmall) <a href="https://twitter.com/danielmall/status/636893941028290560">August 27, 2015</a></blockquote>

            <p><script async src="//platform.twitter.com/widgets.js" charset="utf-8"></script></p>

            <p>I expected a handful of answers and opinions but was instead bombarded by a wealth of great thoughts, questions, and resources. </p>

            <p>Here are a few interesting things that I learned, in no particular order:</p>

            <ul>
                <li>I certainly couldn&rsquo;t share the full context of my questions within 140 characters, but I was originally curious about the <em>minimum</em> number of hours it takes to be productive as a designer. Most of the replies I received seemed to assume that I was asking about the <em>maximum</em> amount of hours we should be working. Though I&rsquo;m not sure about the particular conclusion to draw from that, I can&rsquo;t help but think it&rsquo;s related to <a href="http://www.newyorker.com/magazine/2014/01/27/the-cult-of-overwork">the cult of overwork</a> that seems to be so pervasive within our agencies and shops.</li>
                <li>Many suggested the fact that it&rsquo;s less about a minimum or maximum amount of hours and instead about protecting your productive times (thanks <a href="https://twitter.com/jasonfried/status/636910619430551554">Jason</a>, <a href="https://twitter.com/cadler/status/636895987630387200">Charles</a>, and <a href="https://twitter.com/davatron5000/status/636901281315995649">Dave</a>!). I certainly agree; my most productive time tends to be between 5am and 7am. But, I discovered that sweet spot after many years of trying many different things. My apprentices don&rsquo;t have years of experience to reflect on. Even though I suggest a lot of trial and error, we have to start somewhere. So what&rsquo;s a good starting point?</li>
                <li>For <a href="https://en.wikipedia.org/wiki/Knowledge_worker">knowledge workers</a> like us, <a href="http://lunar.lostgarden.com/Rules%20of%20Productivity.pdf">performance declines after 35 hours</a> (thanks, <a href="http://shauninman.com/">Shaun</a>!).</li>
                <li>Lots of data points to the fact that working more than 40-hours a week is often a waste. For example, Stanford economics professor John Pencavel found that <a href="http://www.economist.com/blogs/freeexchange/2014/12/working-hours">after 50-hours a week, output rises at a decreasing rate</a> (thanks <a href="http://paularmstrong.me/">Paul</a>!). According to his research, &ldquo;output at 70 hours of work differed little from output at 56 hours.&rdquo;</li>
                <li>Interesting historical tidbit: <a href="http://www.todayifoundout.com/index.php/2011/05/why-a-typical-work-day-is-eight-hours-long/">The eight-hour day was first suggested by socialist Robert Owen in 1817</a>. He proposed splitting the typical day in thirds: 8 hours for sleep, 8 hours for recreation, and 8 hours for work. A century later, <a href="http://www.history.com/this-day-in-history/ford-factory-workers-get-40-hour-week">the Ford Motor Company adopted a 40-hour work week</a> (thanks <a href="http://kylefiedler.com/">Kyle</a>!).</li>
            </ul>

            <p>My conclusion, so far: I think I&rsquo;ll start by suggesting and shooting for <strong>35 hours a week</strong>, certainly not much less and probably not much more. How do you handle your work week?</p>

