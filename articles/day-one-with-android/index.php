			<h1 class="article-header-title">Day One With Android</h1>
			
			<p><a href="http://v2.danielmall.com/archives/2007/06/30/hi_phone.php">I bought an iPhone the day it came out</a>. After nine years of using iOS1 &ndash; iOS7 across iPhones 1 &ndash; 6, I&rsquo;m <a href="https://twitter.com/danielmall/statuses/617681523744055296">switching to Android</a>, simply to gain a new perspective. I&rsquo;m trading my <a href="http://www.apple.com/iphone-6/">iPhone 6</a> and <a href="http://www.apple.com/watch/">Apple Watch</a> for a <a href="https://oneplus.net/one">OnePlus One</a> and a <a href="https://moto360.motorola.com/">Moto 360</a>.</p>

			<div class="illo">
				<img src="i/old-new.jpg" alt="" />
			</div><!-- .illo -->

			<p>For reference, I&rsquo;m not switching because I&rsquo;m fed up with Apple. In fact, quite the contrary: I&rsquo;m think the iPhone is the best smartphone in the world.</p>
			<p>Why switch? As someone who claims to be able to design for people using any type of device, I know shockingly little about Android. I did a roundtable discussion at the <a href="http://environmentsforhumans.com/2015/web-sherpa-summit/">Sherpa Summit</a> a few months ago where my friend <a href="http://hellofisher.com/">Steve Fisher</a> mentioned that he switches operating systems once a year. Following Steve&rsquo;s lead would certainly help me to understand a different perspective.</p>

			<p>So, here starts (what I think will be at least) a year of using an Android device as my primary device. Some initial thoughts after a day of use, in no particular order:</p>

			<p><strong>Android or OnePlus One?</strong> This being my first Android device, I&rsquo;m not quite sure what the distinction between what&rsquo;s Android and what&rsquo;s specific to the phone model (OnePlus One). Forgive me if I&rsquo;m mixing up the two.</p>

			<div class="col50 clearfix illo">
				<div class="col col1">
					<img src="i/copy-url.png" alt="" />
				</div><!-- .col -->
				<div class="col col2">
					<p><strong>Macro and micro.</strong> Overall, iOS still feels more intentional. It seems more thought through. I never once needed documentation to figure out the iPhone, but my first two hours consisted of searching the internet for everything from turning the phone on silent (which is actually incredibly overcomplicated on the OnePlus One) to how to set a passcode. iOS feels like I&rsquo;ve known it forever, even from the moment I first used it. That said, some of the smaller features that I&rsquo;ve learned—not intuited, but learned—are ones I wish the iPhone had and seem like a no-brainer. For example, if you long-press the address bar in Chrome, you get the option to Copy URL, which makes so much sense. Also, <a href="https://www.youtube.com/watch?v=Yq0mO5u2doA">Face Unlock</a>!</p>
				</div><!-- .col -->
			</div><!-- .col50 -->
			
			
			<p id="voice-control"><strong>Voice control.</strong> This is the one I was waiting for. I&rsquo;ve been using my <a href="http://amzn.to/1S1DvTn">Amazon Echo</a> for the past few months, especially for the voice control. It&rsquo;s a feature I never used much before because Siri, well, stinks. (I&rsquo;ll write more about the Echo in a separate post.) But the Android voice recognition seems to know just what to do, without any training. It corrects phrases it &ldquo;heard&rdquo; to what makes more sense, somehow. It knows to do a web search when I say, &ldquo;Launch rocket&rdquo; but opens the app when I say, &ldquo;Launch Facebook.&rdquo; The Android voice control is magic, just like it should be.</p>

			<div class="col50 illo">
				<div class="col col1">
					<img src="i/ok-google.png" alt="Android voice control results screen" />
				</div><!-- .col -->
				<div class="col col2">
					<img src="i/siri.png" alt="Siri voice control results screen" />
				</div><!-- .col -->
				<div class="clearfix">
					<p class="caption">The Android voice control results screen vs. the Siri voice control results screen.</p>
				</div>
			</div><!-- .col50 -->

			<p id="tighter-google-integration"><strong>Tighter Google integration? Where?</strong> I use Gmail, Google Calendar, Chrome, Google Docs, Google Sheets, Hangouts and more, so it makes sense that a Google device would have tighter Google integration than an Apple one. Or at least that&rsquo;s what a few people told me. So far, I haven&rsquo;t seen any evidence of that; Google apps run the same on my OnePlus One as they did on my iPhone 7 (no better, no worse).</p>

			<p id="wearables"><strong>Wearables.</strong> I&rsquo;ll write more about wearables in the near future, but I put the Moto 360 back in its box after 5 hours and requested a refund. The voice control was pretty hit or miss. In that timeframe, the battery had dropped to 44%. The clincher was that <a href="https://www.reddit.com/r/moto360/comments/33fand/latest_android_wear_update_breaks_360/">the current version of Android Wear is pretty buggy</a> with the 360, and a reboot and/or reset didn&rsquo;t seem to help. Just about every action ended in, &ldquo;Unfortunately, Android Wear has stopped.&rdquo; <a href="http://snook.ca/archives/review/moto360-pebble">Snook does a great job reviewing the Moto 360</a>, and my thoughts are pretty similar to his. Also, <a href="http://www.elliotjaystocks.com/blog/1-month-with-the-apple-watch/">Elliot Jay Stocks &rsquo; review</a> and <a href="https://medium.com/@vanschneider/12-things-you-should-know-about-the-apple-watch-602b7257202d">Tobias Van Schneider&rsquo;s review</a> kinda sum up my thoughts on wearables in general. More thoughts on this another time.</p>

			<p id="customization"><strong>Too much customization.</strong> I can customize everything on my OnePlus One. Fonts, icons, color palettes, wallpaper, boot animations, sounds, whole themes, etc. I can even mix and match by taking icons from one theme and mixing it with fonts from another. That sounds like a dream for a designer, but it actually feels like too much. I appreciate that Apple picked a style, and that&rsquo;s that.</p>

			<p id="apps"><strong>Apps.</strong> I don&rsquo;t know which apps are good. I&rsquo;m realizing that I find out about new, good apps from my Twitter and Facebook friends, most of whom use iPhones. I don&rsquo;t get much news about good Android apps, nor do I really know where to look, other than simple Google searches. What alarm clock is best? Is this battery saver app any good? Seems like the only thing I can count on is guess and check.</p>
			
			<p id="auto-audio"><strong>Auto audio.</strong> I plugged the OnePlus One into my car&rsquo;s USB port and fired up Rdio. No playback. I turn on Bluetooth and tell the car to search for a new Bluetooth device. Nothing. Hrmph.</p>
			
			<p>All in all, I&rsquo;ve ditched a smartwatch all together and gone back to wearing my <a href="http://www.qlocktwo.com/info_w.php?lang=de">Qlocktwo W</a> or <a href="http://braun-clocks.com/watch/BN0021BKBKG">Braun classic</a>. On the phone side, I dig my OnePlus so far and look forward to learning more about it, though I realize it&rsquo;s certainly not a perfect device. That said, I seem to find myself carrying my old iPhone around in my pocket or my bag like a security blanket I can&rsquo;t wean off. I&rsquo;m hoping that goes away soon enough.</p>

			<p>Have you made a device switch recently? How are you handling it?</p>
			
