                <p>We prepped together as a team at dinner the night before. Kevin, being <a href="http://goodkickoffmeetings.com/">the meeting guru</a> he is, advised that the schedule looked too tight to be doable, especially the after-lunch sessions. I brushed off his warnings. (He was right.)</p>

                <p>We had a great conversation, especially during the Content Discussion and Design Studio sessions. However, one question kept coming up that still remained unanswered at the end of the Design Studio: what&rsquo;s the right balance of value for the user vs. information about <abbr>RIF</abbr> that should exist on the site? Of course, the <abbr>RIF</abbr> site should be a place where people can learn about the organization and all it does to benefit kids in need. However, people generally don&rsquo;t like being broadcasted at; content on the site needs to provide value to its <span class="glue">audience&mdash;in</span> this case, <span class="glue">parents&mdash;arguably</span> even if it&rsquo;s not about <abbr>RIF</abbr>.</p>

                <p>This was my favorite part of the meeting: knowing that we still didn&rsquo;t have this answer fully solidified, we decided to break the rest of the agenda and instead refocus as much of the rest of the meeting as we could into answering this question and all the details that surrounded it.</p> 

