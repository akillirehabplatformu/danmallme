				<p>Last May, I got an email from <a href="http://rif.org/">Reading Is Fundamental</a>, the largest children&rsquo;s literacy nonprofit in the United States, about helping them with a new site. They give free books to kids who need them. I can certainly recall growing up and seeing many of <abbr>RIF</abbr>&rsquo;s <a href="http://www.youtube.com/playlist?list=PL34A87AFB20262FDE&feature=plcp">great <abbr title="Public Service Announcement">PSA</abbr>s</a> (as I hope you can), so I was really excited to receive a note about working with them. A few conversations later, <a href="http://superfriend.ly/">SuperFriendly</a> and <abbr>RIF</abbr> are embarking on a journey together towards a new site.</p>
                

                <p>It&rsquo;s a great thing when clients are willing to be brave with you. I mentioned in passing that it&rsquo;d be great to design this new site publicly (to borrow <a href="http://chriscoyier.net/2012/09/23/working-in-public/">a phrase from Chris Coyier</a>), as it&rsquo;d be great for both the design community <span class="glue">but&mdash;more</span> <span class="glue">importantly&mdash;also serve</span> <abbr>RIF</abbr> &rsquo;s mission to give away great resources to those who might need them. That also includes other non-profits that might not know what goes into the process of  making a site. Not only did <abbr>RIF</abbr> agree with this notion; they were bursting at the seams to try this for our process together. Win!</p>

                <p>To make sure we can do the best job we can for <abbr>RIF</abbr> and their audience, we&rsquo;ve assembled a dream team of partners to knock this out of the park. <a href="http://kevinmhoffman.com/">Kevin Hoffman</a> will be tackling information architecture and content strategy. <a href="http://esbueno.noahstokes.com/">Noah Stokes</a> and his crew at <a href="http://hellobold.com/">Bold</a> will be coding the front-end and helping <abbr>RIF</abbr> migrate to <a href="http://expressionengine.com/">ExpressionEngine</a>. SuperFriendly will be handling design and overall project management: designer <a href="http://twitter.com/jluciano215">Josh Luciano</a> will be helping to craft an appropriate look and feel, project manager <a href="http://mattecook.com/">Matt Cook</a> will be making sure everything runs smoothly and that all the pieces fit together nicely, and I&rsquo;ll be concentrating on the overall creative direction &amp; strategy to make sure we&rsquo;re making the best possible site for <abbr>RIF</abbr> and that the quality of all of our work is something we can be proud of. Over the next few months, we&rsquo;ll be sharing updates about this project: the good and the not-so-good, the failures and the victories.</p>
                
                <p>First up: the kickoff meeting.</p>


