			<h1 class="article-header-title">Oil Change &amp; Pizza</h1>
			
			<p>I once saw a sign that said, &ldquo;Oil change &amp; Pizza.&rdquo;</p>

            <p>Huh. Oil change. And pizza. </p>

            <p>It made me think that the pizza couldn&rsquo;t be very good. It also made me think that maybe they&rsquo;re not great at oil changes either.</p>

            <p>Agencies do this all the time. &ldquo;Full service,&rdquo; they say. Shorthand for &ldquo;we do it all!&rdquo; Branding, video production, packaging, web design, marketing, SEO. What are they actually good at? What are they actually amazing at?</p>

            <p>My favorite pizza place in the world is <a href="https://twitter.com/difara">Di Fara Pizza</a> in Brooklyn. The 80-year old owner still makes all the pizzas there by hand while his kids work in the back. There&rsquo;s only one type of pizza there: the classic margherita pie. If you want pepperoni, you&rsquo;re free to go somewhere else. They have a limited amount of ingredients every day, and they close when they run out, whether that&rsquo;s 11pm or 3pm. By all indications, Di Fara is making a killing.</p>

            <p>It&rsquo;s no secret what Di Fara is good at. (No, they don&rsquo;t do oil changes.) Customers often endure one- to two-hour waits just to get a $5 slice. They&rsquo;re a shining example of the advice we hear all too often in the design and tech industries: do one thing, and do it well.</p>

            <p>When I coach agency owners, the conversations in our first sessions often revolve around positioning. I start by asking three questions:</p>

            <ol>
                <li>What does your agency do better than any other agency in the world?</li>
                <li>If all your bills were paid for in perpetuity, what kind of work would your agency be Doing?</li>
                <li>What work are your clients asking you for?</li>
            </ol>

            <p>The more closely aligned the answer to those questions, the better your positioning is. If you 1) build apps for cat lovers better than anyone else, 2) love building apps for cat lovers, and 3) you&rsquo;re booked solid until the end of next year building apps for cat lovers, chances are pretty high that you&rsquo;re running a lucrative business that you only love. If instead you 1) do editorial illustration better than anyone but 2) you&rsquo;d really love to be stop motion animation and 3) your clients are coming to you for WordPress sites, your likely either not very happy with your work situation or you&rsquo;re having trouble making ends meet financially.</p>

            <p>What can you do about this?</p>

            <ol>
                <li><strong>Specialize.</strong> Put in the effort to be the best—or one of—at something. "Full service&rdquo; is not a positive. Business strategist Tim Williams <a href="http://www.ignitiongroup.com/propulsion-blog-post/client-ever-buys-wide-range/">advises</a>, &ldquo;&hellip;you&rsquo;re working against the best interests of your firm when you put &lsquo;Wide range of experience&rsquo; on your website, because that&rsquo;s clearly not what the best clients are buying. Agencies, ever fearful of following their own advice about strategic focus, use the words &lsquo;full service&rsquo; and &lsquo;wide variety&rsquo; so they can cast a wide net. But a wide net catches only small fish. It&rsquo;s not the way to land a big barracuda.&rdquo;</li>
                <li><strong>Narrow.</strong> Let&rsquo;s say you put in the effort to be an amazing designer. Congrats! But, there are enough designers out there that the odds of you being the best in the world are slim. Narrow that positioning. If you can&rsquo;t be the best designer in the world, start by being the best designer in your town and scoop up all those local design jobs. You can narrow by geography (the best designer in Conshohocken), by vertical (the best designer in the airline industry, by season (the best wedding invitation designer), and countless other criteria. You can even combine them (the best wedding invitation designer in Conshohocken for airline-themed weddings). Make a name for yourself so that, when clients need the type of work you do, you come up at the top of the list. Even better: make sure you <em>are</em> the list.</li>
            </ol>

            <p>The &ldquo;<a href="http://workingwithmckinsey.blogspot.com/2013/01/t-shaped-problem-solving-at-mckinsey.html">T-shaped</a>&rdquo; concept by McKinsey &amp; Company is a great one for freelancers, but it certainly applies to agencies just as much. Possess the breadth of skills that make you and your team versatile enough to tackle anything, but advertise the depth of knowledge that makes you the perfect choice to solve your clients&rsquo; problems. </p>

            <p>In his book <cite><a href="http://amzn.to/20jE0wp">Linchpin</a></cite>, Seth Godin says, &ldquo;What makes a great museum is the stuff that&rsquo;s not on the walls.&rdquo; Similarly, one could argue that what makes a great agency are the things they <em>don&rsquo;t</em> do.</p>

            <p>So. Oil change. Or pizza. Just not both. </p>


