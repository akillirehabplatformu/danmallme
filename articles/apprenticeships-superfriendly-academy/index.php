            <p>In looking to fill vacant job openings, many tech companies look to the upper echelon of this graph. That results in the skilled becoming even more skilled as they move from job to job, creating a wider gap between those at the top and those at the bottom.</p>

            <p>I believe there&rsquo;s a great solution to bridge this ever-widening gap, and it&rsquo;s called <em>apprenticeship</em>.</p>

            <h2 id="apprenticeship"><a class="named-anchor" href="#apprenticeship">About apprenticeship</a></h2>

            <p><a href="https://en.wikipedia.org/wiki/Apprenticeship">Wikipedia defines apprenticeship</a> as &ldquo;a system of training a new generation of practitioners of a trade or profession with on-the-job training and often some accompanying study.&rdquo; Apprenticeship originated in medieval times and were largely governed by guilds.</p>

            <p>Here&rsquo;s how a blacksmith apprenticeship would work in those times. A parent would indenture their child&mdash;little Johnny, typically around 10 years old&mdash;to a local master blacksmith. Johnny would live with the blacksmith for about 7 years, learning the trade of blacksmithing but also learning the business of blacksmith, as well as life skills like cooking, cleaning, and generally how to function in society. Teaching <em>all</em> of these responsibilities were under the purview of the blacksmith, not just the craft. The blacksmith was responsible for teaching Johnny how to be a professional. This grooming of professionalism is what&rsquo;s missing in the common internship today, which is why I believe in apprenticeships as a more productive model.</p>

            <p>(For a deeper dive, interaction designer <a href="http://twitter.com/IvanaMcConnell">Ivana McConnell</a> has written &ldquo;<a href="https://louderthanten.com/coax/apprenticeship-an-internship-replacement">Apprenticeship, an internship replacement</a>,&rdquo; a fantastic essay about the apprenticeship model.)</p>

            <p>Apprenticeships are common among many different professions:</p>

            <ul>
                <li>Many tattoo artists break into that profession through apprenticeship. Pittsburgh tattoo artist Jason Lambert wrote <a href="https://tattoozen.wordpress.com/2009/04/08/before-you-ask-me-for-an-apprenticeship/">an amazing list of do&rsquo;s and don&rsquo;ts for aspiring apprentices</a> that&rsquo;s definitely applicable outside of the tattoo world.</li>
                <li>Many chefs start out as apprentices. <cite><a href="http://amzn.to/1Ytoyhv">The Apprentice: My Life in the Kitchen</a></cite> is a memoir from chef Jacques Pepin about his start in the culinary world.</li>
                <li>Apprenticeship is extremely common in the field of architecture. Frank Lloyd Wright&rsquo;s <a href="http://www.steinerag.com/flw/Books/f1982-04.htm#Apprentices">Letters to Apprentices</a> collect the way the master architect relayed his genius to those who worked with him.</li>
                <li>Famed theologian Martin Luther recorded a series of informal conversations he had with apprentices and other dinner guest in the book <a href="http://www.ccel.org/ccel/luther/tabletalk.html">Table Talk</a>. They discuss theology, government, beliefs, academia, and much more.</li>
            </ul>

            <p>The design and development industry is full of internships that intend to teach good design and development practices, but it&rsquo;s extremely limited in apprenticeships whose purpose is to create well-rounded professionals. </p>

            <h2 id="academy"><a class="named-anchor" href="#academy">About SuperFriendly Academy</a></h2>

            <p>Since December 2012, I&rsquo;ve been silently&mdash;and <a href="https://creativemornings.com/talks/dan-mall" title="Dan Mall talking about apprenticeships at Creative Mornings">sometimes not-so-silently</a>&mdash;running <a href="http://superfriend.ly/academy/">SuperFriendly Academy</a>, a web design &amp; web development apprenticeship that teaches both core skills and professionalism, but leans more toward the latter. It&rsquo;s a 9-month program, created for people who have little to no experience with design or development but are looking :</p>

            <ul>
                <li><strong>Months 1&ndash;3</strong> are basic training. Because everyone who goes through this apprenticeship knows very little about the craft of design or development, this first quarter gets them up to speed. On the first day of an apprenticeship, we talk about things like what a <abbr title="Uniform Resource Location">URL</abbr> is and <a href="http://preethikasireddy.me/?p=174&amp;fb_ref=Default">how the web works</a> in general. For design apprentices, we walk through the toolbars of Sketch and Photoshop, one tool at a time to talk about what they do, For development apprentices, we start by talking about the anatomy of <abbr title="HyperText Markup Language">HTML</abbr> tags. And the curriculum builds from there, getting more and more tailored to the individual as we progress.<br /><br />Before we even get to this point, I also ask each apprentice to save enough money to pay for about 3 months of their expenses. I don&rsquo;t pay apprentices to start, because they&rsquo;re not valuable to me at this point since they don&rsquo;t have much knowledge about this world yet. I don&rsquo;t ask them to pay to go through the apprenticeship, so we both break even.  Because this is likely the first <a href="https://en.wikipedia.org/wiki/Knowledge_worker">knowledge work</a> job an apprentice may have had, it tends to be mentally taxing, thereby making it fairly difficult to maintain a part-time nights/weekends job while doing the apprenticeship. This 3-month savings request is often where I see interest wane, because not everyone can afford to forego income for that amount of time. (I&rsquo;m working on ways to solve this problem to make this apprenticeship more feasible for more folks. If you have suggestions on how I can better do that, <a href="/contact/">please let me know</a>.)</li>
                <li><strong>Months 3&ndash;6</strong> are about practice. By this point, the apprentice has enough of an understanding of design and/or code that I can start putting them on client projects. We start small; a typical first paying assignment for an apprentices might be to design or code a very small section of a site or app, like a footer or a search interaction. This work is always supervised by someone who is ultimately responsible for the quality of that work, either myself or another <a href="http://superfriend.ly/#superfriends">SuperFriend</a>. Because some of the apprentices go on to freelance after their apprenticeships are over, I treat them like subcontractors on all projects. I ask them to <a href="https://abookapart.com/products/pricing-design" title="&ldquo;Pricing Design,&rdquo; by Dan Mall on A Book Apart">price</a> those projects for me as if I was hiring them as a professional, and we work together on discussing what good pricing looks like if they under- or overbid. The more their skills increase during this timeframe, the bigger responsibilities I can assign to them, and the more they get paid. This time period is crucial in teaching the most important parts of professionalism. Almost every apprentice picks up the design and code stuff really quickly, but some of them flop with softer things like meeting deadlines, setting expectations for colleagues and clients, articulating their work well, and other similar skills.</li>
                <li><strong>Months 6&ndash;9</strong> are about job readiness. The previous months were mostly about practicing professionalism and amassing skills and projects, and now it&rsquo;s time to turn those things into a launchpad. We make lists of the places the apprentice might want to work, and we start preparing portfolios targeted at those places. We do mock interviews to get them ready for the job they&rsquo;ve worked so hard to get. I help coach them through negotiating salaries to make sure they&rsquo;re landing in the right place with the right setup.</li>
            </ul>

            <h2 id="why"><a class="named-anchor" href="#why">Why have apprentices?</a></h2>

            <p>While you may be sold on the altruism of taking on an apprentice, you may be having trouble seeing what&rsquo;s in it for you or your company. Fortunately, there are many benefits to investing in an apprentice or even an apprenticeship program:</p>

            <h3>Fishing in a larger pond</h3>

            <p>From a pure numbers perspective, you have lower odds of finding a new hire in the highly competitive market of 40,000 computer science graduates than you are the 8 million unemployed people in this country. The pool is simply larger.</p>

            <h3>Cost-effectiveness</h3>

            <p>It&rsquo;s also way cheaper. Compare the numbers of poaching a highly competitive candidate vs. giving an accomplished apprentice a chance (these numbers assume a 5% annual raise):</p>

