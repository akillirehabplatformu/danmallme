            <p>For one, the competitive candidate will come in at a more demanding salary and is likely to continue <a href="http://www.business.com/human-resources/welcome-to-the-era-of-job-hopping-the-lifecycle-of-the-american-worker/">job hopping after year 3</a>. After that time, you may have to deal with your more competitive candidate leaving, which means you&rsquo;ve lost a good bit of institutional knowledge and also have a hefty replacement search ahead of you.</p>

            <h3>Less investment than you may think</h3>

            <p>As you consider the amount of time you may have to spend training an apprentice over 9 months, you may be skeptical about being able to carve out that effort. But, it&rsquo;s probably less time than you think.</p>

            <p>I&rsquo;ve had ten apprentices under this curriculum, and, on average, the amount of time I&rsquo;ve spent training each of them over 9-months is only <strong>35 hours</strong>. That&rsquo;s about <strong>4 hours/month</strong>, <strong>1 hour/week</strong>, or just <strong>12 minutes</strong> a day. That&rsquo;s right: pretty doable for just about anyone, even taking into account a busy schedule.</p>

            <p>(The big caveat with these numbers is that it doesn&rsquo;t include the amount of time I spend on direction within a project, but, because that time is project-specific, it&rsquo;s work I would have done anyway directing anyone on the project team, so I don&rsquo;t count it as general training time.)</p>

            <p>Regarding money, the average amount I&rsquo;ve paid to an apprentice over 9 months is <strong>$7,000</strong>. That&rsquo;s about <strong>$1166/month</strong>, <strong>$292/week</strong>, <strong>$58/day</strong>, or <strong>$7/hour</strong>. That&rsquo;s pretty affordable for many organizations. And for apprentices, choosing between minimum wage for a food service job and one that helps them invest in their future, I&rsquo;d guess that choice is a no-brainer. </p>

            <h3>Diversity</h3>

            <p>Perhaps the most important reason to invest in training the untrained is that that pool of folks likely doesn&rsquo;t look like you, think like you, work like you, or do much like you. And that&rsquo;s an incredible thing. There&rsquo;s so much sameness that exists in tech today, and that&rsquo;s undoubtedly leading to the unoriginal and uninventive sea of products we see today. Intentionally investing in different perspectives can work wonders for every organization. </p>

            <p>Don&rsquo;t take my word for it; the very tangible results speak for themselves. In their article <a href="http://www.mckinsey.com/business-functions/organization/our-insights/why-diversity-matters">Why Diversity Matters</a>, McKinsey &amp; Company reports that gender-diverse companies are 15% more likely to have financial returns above their respective national industry means, and racially- and ethnically-diverse companies are 35% more likely to have financial returns above their resurrecting national industry means. Apprenticeships aren&rsquo;t just an altruistic endeavor; they can directly impact your bottom line. </p>

            <h2 id="join"><a class="named-anchor" href="#join">Join the cause</a></h2>

            <p>The great news is that there are already many apprenticeships out there for budding designers, developers, and <abbr title="User Experience">UX</abbr> professionals. <a href="https://nerdery.com/">The Nerdery</a> runs <a href="https://blog.nerdery.com/2013/02/user-experience-design-apprenticeship/">an amazing <abbr>UX</abbr> Design apprenticeship</a>, led by <a href="https://twitter.com/fred_beecher">Fred Beecher</a>. <a href="http://www.freshtilledsoil.com/">Fresh Tilled Soil</a> runs a 15-week <abbr title="Apprenticeship in User Experience">AUX</abbr> program, which they just <a href="https://medium.com/fresh-tilled-soil/we-think-product-design-apprenticeships-are-the-future-thats-why-we-re-open-sourcing-ours-bef594d506c8#.t5f68158g">graciously open-sourced</a>. <a href="https://seesparkbox.com/">Sparkbox</a> runs <a href="http://apprentices.seesparkbox.com/">a 6-month apprenticeship</a>. <a href="https://thoughtbot.com/">Thoughtbot</a> runs <a href="http://www.apprentice.io/">a 3-month apprenticeship</a>. <a href="http://www.hugeinc.com/">HUGE</a> runs <a href="http://www.hugeinc.com/schools">a 12-week <abbr>UX</abbr> school</a>.</p>

            <p>As of right now, <a href="http://superfriend.ly/academy/">SuperFriendly Academy</a> is open for applications. Head over to the site and get in touch if you meet the guidelines.</p> 

            <p>There are already great programs out there like this, but there aren&rsquo;t nearly enough. It&rsquo;s about time that changed.</p>

