                    <p>The server does all the work here. (There&rsquo;s a little <abbr title="PHP HyperText Processor">PHP</abbr> action that randomly generates the numbers, but that doesn&rsquo;t really matter for this demo.) This is our barebones, baseline version. For mobile devices, slow connections, sluggish networks, we&rsquo;ve got a lean mean page that doesn&rsquo;t leave you lost.</p>

                    <p>Next, write some styles to get this looking good. Don&rsquo;t change any markup. We&rsquo;re progressively enhancing!</p>

                    <p><img src="pest-css.png" alt="HTML &amp; CSS for Progressively Enhanced Stock Table" /></p>

                    <p>The final bit is to add JavaScript to add real-time functionality. This is where problem #2 often comes in. I need styles that only apply when the JavaScript functionality is available. The way I do this is to add a new CSS file using JavaScript. <a href="http://v2.danielmall.com/archives/2010/04/04/enhanced.php">I&rsquo;ve written about this before</a>, but think it&rsquo;s worth mentioning again.</p>

                    <p>In the <code>&lt;head&gt;</code>, write this just under your main stylesheet: </p>

                    <p><pre class="language-javascript">&lt;script&gt;document.write('&lt;link rel="stylesheet" href="/-/c/enhanced.css" />');&lt;/script&gt;</pre></p>

                    <p>Now you have an entire stylesheet that only loads when JavaScript does. It also does so before the page renders, so none of that <abbr><a href="http://en.wikipedia.org/wiki/Flash_of_unstyled_content">FOUC</a></abbr> crap. For me, that&rsquo;s worth an extra <abbr title="HyperText Transfer Protocol">HTTP</abbr> request. (It may not for you, so there are <a href="http://www.paulirish.com/2009/avoiding-the-fouc-v3/" title="&ldquo;Avoiding the FOUC v3.0,&rdquo; by Paul Irish">other ways to solve this</a>.)</p>

                    <p>In that stylesheet, you can now create styles appropriate to the real-time functionality, like converting that table into a graph and plotting the numerical data.</p>

                    <p>You can also create some JavaScript logic that alters or augments the markup already on the page. This is the other culprit of problem #1. JavaScript is an awful excellent weak powerful language. You can create full-scale software with it or you can use it for DOM manipulation. It&rsquo;s both a sledgehammer and a scalpel. In my experience, the best examples of progressive enhancement go the scalpel route.</p>

                    <p>My JavaScript file for this example does a handful of things to enhance this page, like manipulating the markup for the table-to-graph conversion, adding points real-time, updating the time on the page to show that it&rsquo;s real-time, and removing the prompt to refresh since it&rsquo;s not necessary anymore. (The logic for the real-time data is just randomly generated, but the point of the demo is to show that any type of JavaScript algorithm you create can be used to enhance the existing markup, rather than having to create it in the first place.)</p>

