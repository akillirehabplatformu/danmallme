            <section class="drawer trolling">

                <div class="inner">

                    <h1>On Trolling</h1>

                    <p>I&rsquo;ll be the first to admit that I&rsquo;m trolling, at least a bit. But I&rsquo;m not trying to. </p>

                    <p>I didn&rsquo;t mean to offend anyone. I know that I unfortunately and unitentionally have, and I apologize for that and have apologized to the people I know for certain I&rsquo;ve offended. If anyone&rsquo;s personally offended or hurt by anything there, <a href="/contact/">email me</a> and I&rsquo;ll try my best to make it right. That might mean revising or editing a post. It might mean that I&rsquo;ll work with you to help get your site working well for as many people as possible. Let&rsquo;s talk about it.</p>

                </div><!-- .inner -->

            </section><!-- .trolling -->

            <section class="drawer accountability">

                <div class="inner">

                    <h1>Accountability</h1>

                    <p>Once upon a time, Oakley launched <a href="http://moto.oakley.com/">a website</a> to promote some new goggles. That website weighed in at a whopping <strong>85.4<abbr title="megabyte">MB</abbr></strong>. Phil Hawksworth <a href="http://hawksworx.com/blog/oakleys-monster-page-of-baubles/">wrote about it</a>. Oakley worked on it and <a href="http://hawksworx.com/blog/oakleys-moto-diet/">responded to Phil</a> that they got the page weight down to <strong>13.6<abbr>MB</abbr></strong>. That&rsquo;s a savings of <strong>71.8<abbr>MB</abbr></strong> or <strong>84%</strong>. Andy Clarke <a href="https://twitter.com/Malarkey/status/326426810604724226">worked it out</a>; at $9/<abbr>MB</abbr>, the original site would cost <strong>$768 </strong>to view on an iPhone. The newly optimized site&mdash;though still large&mdash;would cost <strong>$122</strong>, quite literally saving you <strong>$646</strong>. I&rsquo;m sure there&rsquo;s more to the story that we don&rsquo;t know, but the simplified version is that Phil played a part in saving someone <strong>$646</strong>.</p>

                    <p><a href="https://www.google.com/search?q=accountable">Google tells me</a> that &ldquo;accountable&rdquo; means &ldquo;(of a person, organization, or institution) required or expected to justify actions or decisions.&rdquo; Some people justify a site that doesn&rsquo;t work with JavaScript disabled by saying, &ldquo;the site is for people who have JavaScript enabled.&rdquo; Sometimes, that&rsquo;s a good reason and it&rsquo;s true. Sometimes though, it&rsquo;s the most convenient way to dodge a real problem. And sometimes, we don&rsquo;t realize that &ldquo;only for people who have JavaScript enabled&rdquo; also means &ldquo;not for anyone with a Blackberry&rdquo; or &ldquo;not for anyone who works at [old-school organization]&rdquo; or &ldquo;not for people in a developing country&rdquo; or &ldquo;not for people on the Edge network.&rdquo; </p>

                    <p>Lots of people don&rsquo;t know how to build sites that work for as many people as possible. That&rsquo;s more than ok, but don&rsquo;t pretend that it was your plan all along.</p>

                    <p><cite>Sigh, JavaScript</cite> isn&rsquo;t mean to be a hall of shame. It&rsquo;s a litmus test. If I visit your site, turn off JavaScript, and see a dead end, I&rsquo;ll take a screenshot and post it. No malice intended.</p>

                    <p>This isn&rsquo;t about shame. It&rsquo;s about accountability.</p>

                </div><!-- .inner -->

            </section><!-- .accountability -->

            <section class="drawer better-ways">

                <div class="inner">

                    <h1>Better ways to make the point</h1>

                    <p>All that said, I realize that there are better ways to make the point. More than a few have mentioned&mdash;both publicly and privately&mdash;that, rather than posting sites that don&rsquo;t work well, I should be posting sites that do. A &ldquo;sites done right&rdquo; list is a wonderful suggestion, and one that I&rsquo;ll be doing immediately. However, that&rsquo;s not in place of a &ldquo;not working well&rdquo; list; I think it&rsquo;s still valuable to see both ends of the spectrum.</p>

                    <p>To start that, I&rsquo;ve added a <a href="http://sighjavascript.tumblr.com/post/59795895907/google">post to the site</a> about a great example of progressive enhancement done well. This will be the first of many, and they&rsquo;ll be tagged as <code>#yayjavascript</code> from now on, so keep an eye out for them.</p>

                </div><!-- .inner -->

            </section><!-- .better-ways -->

            <section class="drawer even-better-ways">

                <div class="inner">

                    <h1>Even better ways</h1>

                    <p>Better still, my friend Stacey Mulcahy <a href="https://twitter.com/bitchwhocodes/status/372441453915799553">requested a post</a> for how to properly approach progressive enhancement, which I thought was a splendid idea.</p>

                    <p>Before I begin, I&rsquo;ll say that there&rsquo;s been a lot of good posts already written about the topic. Part of my motivation for creating the site was that this isn&rsquo;t a new problem. Some of the best articles on the topic were written long ago. Here are a few of my favorites:</p>

                    <ul>
                        <li>Aaron Gustafson&rsquo;s series on <cite>A List Apart</cite> is one of my all-time go-tos: <a href="http://alistapart.com/article/understandingprogressiveenhancement">Understanding Progressive Enhancement</a>, <a href="http://alistapart.com/article/progressiveenhancementwithcss">Progressive Enhancement with CSS</a>, and <a href="http://alistapart.com/article/progressiveenhancementwithjavascript">Progressive Enhancement with JavaScript</a>.</li>
                        <li><a href="http://jakearchibald.com/2013/progressive-enhancement-still-important/">Progressive enhancement is still important</a>, by Jake Archibald</li>
                        <li><a href="http://coding.smashingmagazine.com/2009/04/22/progressive-enhancement-what-it-is-and-how-to-use-it/">Progressive Enhancement: What It Is, And How To Use It?</a> on Smashing Magazine</li>
                        <li><a href="http://filamentgroup.com/dwpe/">Designing with Progressive Enhancement</a>, by Filament Group</li>
                        <li><a href="http://www.slideshare.net/nzakas/enough-withthejavascriptalready">Enough with the JavaScript Already</a>, by Nicholoas Zakas</li>
                    </ul>

                    <p>Still with me? Let&rsquo;s journey on.</p>

                    <p>There are usually 1 of 2 problems that I often see with sites not properly utilizing good progressive enhancement techniques (which most of the sites on Sigh, JavaScript also suffer from):</p>

                    <ol>
                        <li>All of the content&mdash;read: markup or <abbr title="HyperText Markup Language">HTML</abbr>&mdash;is generated by JavaScript.</li>
                        <li>Or, the markup is good but styles are written in such a way that JavaScript is required to view the site.</li>
                    </ol>

                    <p>This tutorial will show you how to overcome both.</p>

                </div><!-- .inner -->

            </section><!-- .even-better-ways -->

