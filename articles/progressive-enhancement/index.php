                    <h1>A case study</h1>

                    <p>A common argument for requiring JavaScript is that it&rsquo;s ok for &ldquo;web apps.&rdquo; I still don&rsquo;t know what a &ldquo;web app&rdquo; is; I tend to fall in the <a href="https://twitter.com/wilto/status/372080088898367488">Mat Marquis</a>/<a href="http://adactio.com/journal/6246/">Jeremy Keith</a> camp here.</p>

                    <p>So, let&rsquo;s build an &ldquo;app-y&rdquo; example. <a href="https://twitter.com/joemccann/status/372452146538483712">Joe McCann makes a great suggestion</a>: a real-time, trading web application. For the hungry, you can see the final example here: <a href="http://pest.danielmall.com/">pest.danielmall.com</a>. You can also get <a href="https://github.com/danielmall/pest">all of the code on Github</a>, so clone/fork away. (Disclaimer: I know nothing about stocks, so don&rsquo;t fault me for my stock visualization logic here.)</p> 

                    <p><strong>Step 1: write some HTML.</strong> This is already where a lot of people get it wrong; this is where problem #1 happens. Stop writing JavaScript controllers, don&rsquo;t worry about routing yet, put your database schema on hold. Create structural markup for the data. In this case, I&rsquo;ve used a table with 2 columns: time and share price.</p>

