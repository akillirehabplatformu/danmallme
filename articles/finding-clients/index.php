	    <section class="clearfix wrap vanilla">			
			
			<p><strong>Q: How can I find more opportunities to find clients?</strong></p>

			<p><strong>A:</strong> Rumor has it that there was once a time, centuries and centuries ago, where precious stones like rubies and sapphires lay strewn about on the surface of the Earth, free for anyone who came along to pick up and keep. But, as time passed, these jewels became harder to come across, and they ultimately became more valuable due to their scarcity.</p>

			<p>Great clients aren&rsquo;t strewn about across the surface for you to scoop up at your leisure. Like valuable jewels, great clients aren&rsquo;t found; they&rsquo;re earned. Great clients take hard work to earn and keep. You may get lucky and have one drop into your lap, but those are few and far between.</p>

			<p>There are two ways to earn a great client:</p>

			<h2 id="magnet">1. Like a magnet.</h2>

			<p>Magnets draw all metal in the surrounding area to them. When you become one of the best in the world at what you do, you draw great clients to you. When they&rsquo;re looking for the service you offer, they can&rsquo;t help but come across your name. When you need a title sequence for your movie, you call <a href="https://www.imaginaryforces.com/">Imaginary Forces</a>. If you need some hand-lettering, you drop <a href="http://jessicahische.is/">Jessica Hische</a> a note. When you want to advertise your product, you call <a href="http://goodbysilverstein.com/">Goodby Silverstein &amp; Partners</a> or <a href="http://www.bbdo.com/">BBDO</a>. If you need some motion graphics work, you contact <a href="http://www.buck.tv/">Buck</a> or <a href="http://www.psyop.tv/">Psyop</a>. If your organization needs consulting on you mobile content strategy, you call <a href="http://karenmcgrane.com/">Karen McGrane</a>. When you need identity work, you call on <a href="http://www.pentagram.com/">Pentagram</a>. You get the idea.</p>

			<p>Become the best in the world at what you do, and clients will flock to you. This takes years of hard work and dedication, but it pays off.</p>

			<p>(If you&rsquo;re interested, I wrote more about <a href="/articles/how-to-get-the-work-you-want/">getting the work you want</a>.)</p>

			<h2 id="archaeologist">2. Like an archaeologist.</h2>

			<p>For those of us who are still on our way to becoming magnets, we must dig to find good clients. Archaeologists survey for years to find the right digging sites, then spend more years carefully excavating. Not a bad model to follow.</p>

			<p>What kind of work do you want to do? Make a list of clients that satisfy that niche, then go pitch them.</p>

			<p>Let&rsquo;s say your favorite kind of work is identity design for flower shops.</p>

			<ol>
				<li>Make a list of 20 flower shops in your area.</li>
				<li>Do some research to find out how much revenue they generate in a month&mdash;either by using publicly available data or even just asking&mdash;and put together an identity package that you price at a fraction of that revenue that will still make you a good living.</li>
				<li>Call each one and ask for 30 minutes of their time.</li>
				<li>Pitch the heck out of them. Make an undeniable case for how your work for them will reap ten times the amount they&rsquo;d be spending on you.</li>
			</ol>

			<p>Soon enough, you&rsquo;ll have a stellar portfolio of flower shop identity packages, and you&rsquo;ll become the flower shop identity magnet you were hoping to be all along.</p>

			<p>Get pitching!</p>

