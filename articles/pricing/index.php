			<p><strong>Q: How much should I charge for my work?</strong></p>

			<p><strong>A:</strong> I have two young daughters.</p>

			<p>Charlotte, my one-year-old, is just learning to talk. She repeats everything she hears. She doesn&rsquo;t quite have the sounds exactly right yet, but she&rsquo;s getting close. I know that &ldquo;bata&rdquo; means &ldquo;bottle&rdquo; and &ldquo;kee ka&rdquo; means &ldquo;kitty cat.&rdquo; She imitates people who have been talking for decades, and that&rsquo;s how she&rsquo;ll learn to talk. No parent teaches their kid how to talk through etymological discourse and verb conjugation. While it may be logical, it&rsquo;s not practical. Kids learn through hearing and imitating.</p>

			<p>Sidda, my three-year-old, has a pretty large vocabulary by now. She learned to talk in the same way: by imitating. She doesn&rsquo;t know what some words mean, like &ldquo;because&rdquo; or &ldquo;is,&rdquo; but she says them anyway because that&rsquo;s how the people that she imitates talk.</p>

			<p>Once she got that down, she started to question some of the words she knows or hears. &ldquo;Dad, what does &lsquo;tart&rsquo; mean?&rdquo; &ldquo;Mom, what is an &lsquo;insect?&rsquo;&rdquo;</p>

			<p>Next, she started to experiment to see what would happen when she put words together in different configurations. Some turn out great; others are duds. For example: &ldquo;Wanna know why I call clementines &lsquo;times?&rsquo; Because I eat them all the time.&rdquo; <a href="https://twitter.com/SiddaleeMall">And so on</a>.</p>

			<p>What does this have to do with pricing? Well, dear design student, you may be grown in age, but you&rsquo;re still a little design baby in experience. And learning to price is the same thing as learning to talk. Here are the steps:</p>

			<ol>
				<li>Imitate.</li>
				<li>Question.</li>
				<li>Experiment.</li>
			</ol>

			<h2 id="imitate">Step 1: Imitate</h2>

			<p>For your first paying gig, do what everybody else does: pick an hourly rate. There&rsquo;s no wrong rate the first time; pick a number out of thin air. (Hint: that&rsquo;s how everyone does it.)</p>

			<p>I&rsquo;ll start you off. Most professional designers and developers that have a few years under their belts charge somewhere between $70/hour &ndash; $120/hour. (Remember the hint. There&rsquo;s usually no rationale for these rates, except that that&rsquo;s what people have been charging for years.) Since you&rsquo;re a student, you probably don&rsquo;t have the skill or experience to compete at that rate, so something a bit lower will be more appropriate.</p>

			<p><strong>Start at $25/hour &ndash; $50/hour</strong>. It&rsquo;s more than you were making at American Eagle last summer, so it&rsquo;s a good starting point because you&rsquo;re moving up in the world <em>and</em> your customers won&rsquo;t feel like they&rsquo;re being ripped off.</p>

			<p>Do the job. Spend a weekend (20 hours-ish) designing and building that one-page brochure website for your friend&rsquo;s mom. Earn $700 at your $35/hour rate. This is a happy ending to your first project.</p>

			<h2 id="question">Step 2: Question</h2>

			<p>On to the next project. Should you charge the same rate ($35/hour) that you charged for the previous gig?</p>

			<p>Let&rsquo;s evaluate what you gave up and what you gained. You gave up a whole weekend for $700. You could have seen a movie, played a video game, went to a club, gotten hammered, went on a date, or done anything of the things you crazy kids do nowadays. But instead, you sat at your computer and made a thing for someone. Was that worth $700? What does your gut say?</p>

			<p>If your gut says yes, then $35/hour is a good rate for you (for now).</p>

			<p>If your gut says no, then $35/hour is too low. So, what&rsquo;s your weekend worth to you? $1,000? $1,500? $2,000? Let&rsquo;s say you&rsquo;d give up your weekend for $1,500, but not a penny less. That means your rate should be $75/hour ($1500 &divide; 20 hours), because you&rsquo;d rather go see a movie for anything less.</p>

			<p>If that feels too nebulous, here&rsquo;s a tip: Instead of picking a dollar amount, think of something you&rsquo;d trade. I call it object value pricing. If your friend&rsquo;s mom wanted you to design and build a one-page website, would you do it for a fast food dinner? What about a new 42" TV? A new camera? That Ikea sofa you&rsquo;ve been meaning to get?</p>

			<p>Let&rsquo;s say you would do it for the camera. A Canon 7D Mark II is about $1,800, so you could say you&rsquo;d do the project if:</p>

			<ol>
				<li>She bought you the camera, or…</li>
				<li>She paid a fixed price of $1,800, or…</li>
				<li>She paid you an hourly rate of $90/hour (knowing that it would take you about 20 hours)</li>
			</ol>

			<p>Do this a couple of times, and you&rsquo;ll start to get a sense of a few things:</p>

			<ul>
				<li><strong>You&rsquo;ll see how well you scope.</strong> Are you constantly thinking something will take you 20 hours and finding it takes you 40 hours instead? If so, double your hours the next time you estimate.</li>
				<li><strong>You&rsquo;ll start to realize what&rsquo;s important to you.</strong> What&rsquo;s the price for your weekends? For your holidays? For your sleep? For work that you hate doing?</li>
				<li><strong>You&rsquo;ll develop your gut feeling about these things.</strong> The most experienced freelancers and business owners I know can hear about the job and price it on the spot without any spreadsheets and get insanely close to what a spreadsheet may tell them anyway. That comes with practice.</li>
			</ul>

			<h2 id="experiment">Step 3: Experiment</h2>

			<p>Once you start to develop this gut-level Spidey-sense about pricing, you can play around with things. I once had a customer that wanted to book me 6 months out. I didn&rsquo;t want to commit so far out, so I said he&rsquo;d have to pay a non-refundable <em>additional</em> fee&mdash;not a deposit, an additional fee&mdash;of $10,000 dollars to lock in the dates. He never responded to that email, but I found a check for $10,000 in my mailbox the next day that he overnighted. You&rsquo;ll be amazed what people are willing to pay when they really want to work with you.</p>

			<p>Here are some things you could experiment with:</p>

			<ul>
				<li>Double your rate for your next customer.</li>
				<li>If you currently bill hourly, try a fixed price next time. If you currently charge fixed prices, try hourly. See which one you like better, and why.</li>
				<li>See how much extra a customer will pay for things like early delivery, exclusivity, a la carte features, and more.</li>			
			</ul>

			<p>The experiments are endless. But it all has to start somewhere. So, get to it. How much are you gonna charge?</p>

