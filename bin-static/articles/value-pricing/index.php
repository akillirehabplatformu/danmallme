			<h1><span class="cap">V</span>alue Pricing</h1>
			
			<p>The most common questions I get asked at conferences and over email and Twitter are related to pricing. How much should I charge? How do I calculate my rate? How much is too much or too little? What do I do when the client has a lower budget than I want?</p>

			<p>It&rsquo;s easy to find a wealth of Photoshop tutorials or blog posts about <abbr title="Cascading Style Sheets">CSS</abbr> techniques online, but there&rsquo;s not nearly as much written about the business side of design. Admittedly, that&rsquo;s partly because it&rsquo;s difficult and rarely “one size fits all,” but that shouldn&rsquo;t stop us from trying. </p>

			<p>If you&rsquo;re curious about some alternative ways to price your services, Jason and I will be talking about just that on <a href="http://businessologyshow.biz/wordpress/value-pricing-with-daniel-mall-and-jason-blumer/">the upcoming episode</a> of The Businessology Show this Thursday, February 28 2013 at 2 <abbr title="Post Meridian">PM</abbr> <abbr title="Eastern Standard Time">EST</abbr> . We want to make this a really great resource for this topic, and, to do that, we&rsquo;d love your input. What types of things do you want to hear us talk about, and what questions would you love answered? We&rsquo;d love to hear from you, so here are a number of ways you can let us know what&rsquo;s important to you:</p>

