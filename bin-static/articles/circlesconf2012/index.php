			<h1 class="phark logo">Circle Conference 2012</h1>
            
            <p class="lede">Earlier this month, I had the opportunity to be a part of <a href="http://circlesconference.com/">Circles Conference</a>, a wonderful event targeted at non-profits and religious organizations, put together by <a href="http://ismaelburciaga.com/">Ismael Burciaga</a>. It always amazes me how, although never planned, conference talks seem to have a common thread woven throughout. Here were the lasting impressions I had from each great talk:</p>
            
            <ul class="speakers">
                <li><a href="http://kylesteed.com/">Kyle Steed</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_kyle_steed.php">Audio</a> talked about the need for simplicity. When I asked him how he chooses clients, he remarked, &ldquo;I want to work with people that have a purpose and a vision.</li>
                <li><a href="http://www.beautifulidea.us/">Evie Shaffer</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_evie_shaffer.php">Audio</a> talked about purpose. My favorite line from her talk (because it really hit home for me: &ldquo;Prestige is the enemy of passion.&rdquo;</li>
                <li>If there was one guy that I could work for, it would be <a href="http://joshuablankenship.com/">Joshua Blankenship</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_joshua_blankenship.php">Audio</a>. He reminded us that we&rsquo;re wizards; our skills shouldn&rsquo;t be taken for granted. We should be using those skills to help people.</li>
                <li><a href="http://cargocollective.com/sirmon">Paul Sirmon</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_paul_sirmon.php">Audio</a> shared some great advice on branding and client work in general. Here&rsquo;s one of his great strategies: &ldquo;Show your client what they want, then show them what they need.&rdquo;</li>
    			<li><a href="http://noahstokes.com/">Noah Stokes</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_noah_stokes.php">Audio</a> revealed the secrets of <span class="hashtag">#pForm</span>.</li>
    			<li><a href="http://www.promisetangeman.com/">Promise Tangeman</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_promise_tangeman.php">Audio</a> gave us ways to conquer the negativity that often triumphs over our self-esteem and self-confidence.</li>
    			<li>My talk was called <a href="https://speakerdeck.com/u/danielmall/p/designish-circles">Designish</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_dan_mall.php">Audio</a>, and I talked about the soft skills designers should develop to get to the next level.</li>
    			<li>Sadly, I slept in a bit longer than expected the second day of the conference and missed a bit of <a href="http://encourageothers.com/">Kevin Richardson</a>&rsquo;s <a class="phark audio" href="http://circlesconference.com/2012/audio_kevin_richardson.php">Audio</a> talk, but I caught an important part at the end where he summarized the value of taking risks and trying to live life to the fullest.</li>
    			<li><a href="http://ismaelburciaga.com/">Ismael Burciaga</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_ismael_burciaga.php">Audio</a> walked us through his career path and the choices he made to get where he is today.</li>
    			<li><a href="http://www.blainehogan.com/">Blaine Hogan</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_blaine_hogan.php">Audio</a> encouraged us to strive for that human quality that we often forget. He played a wonderful video he directed on why that&rsquo;s so important (trying to track down the video&hellip; let me know in the comments if you&rsquo;ve got a link to it).</li>
    			<li><a href="http://jonathanmoore.com/">Jonathan Moore</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_jonathan_moore.php">Audio</a> challenged us: &ldquo;You don&rsquo;t have to do it right, but just do something.&rdquo;</li>
    			<li><a href="http://www.philcoffman.com/">Phil Coffman</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_phil_coffman.php">Audio</a> reminded us that sometimes the greatest risk&mdash;that pays off big&mdash;is staying put and persevering.</li>
    			<li><a href="http://cameronmoll.com/">Cameron Moll</a> <a class="phark audio" href="http://circlesconference.com/2012/audio_cameron_moll.php">Audio</a> closed the conference. Every speaker was excellent, but Cameron was a different level all together. He inspired us with amazing stories of the work Authentic Jobs was doing with Charity Water. He provoked us to think, no matter what our situation, about what we can do to make a difference.</li>
            </ul>
			
