                <hgroup>
                    <h1>The Post-PSD Era</h1>
                    <h2>A problem of expectations</h2>
                </hgroup>
            </header><!-- .post-header -->
            
            <div class="inner">

                <p>My friend <a href="http://twitter.com/brad_frost">Brad</a> wrote a great post about <a href="http://bradfrostweb.com/blog/post/the-post-psd-era/">entering a post-<abbr title="Photoshop Document">PSD</abbr> era</a>, met by both polarizing support and skepticism. Here&rsquo;s my perspective.</p>

                <p>As I <a href="http://bradfrostweb.com/blog/post/the-post-psd-era/#comment-5903">mentioned</a> in the comments, I think it&rsquo;s an expectation problem. The typical workflow for web design projects is to send a client a &ldquo;preview&rdquo; of the site to approve before beginning development. This &ldquo;preview&rdquo; usually comes in the form of a comp that shows how a page might be laid out and often contains specific, pixel-perfect choices with typography, spacing, images, columns, and other very fine details. The problem is that this says to a client, &ldquo;We&rsquo;re now at a stage where we&rsquo;re focusing on details.&rdquo; It&rsquo;s only natural that their feedback focuses on details.</p>

                <p>The ensuing conversation generally plays out like this:</p>
            
                <dl class="scripted clearfix">
                    <dt>Me</dt>	 	
                        <dd>Here&rsquo;s a comp of what your site will look like.</dd>
                    <dt>Client</dt>	
                        <dd>Great! But what will it look like on my iPhone?</dd>
                    <dt>Me</dt>		
                        <dd>Oh, I&rsquo;ll show you that.</dd>
                    <dt>Client</dt>	
                        <dd>And what about my iPad? Will you show me that too?</dd>
                    <dt>Me</dt>		
                        <dd>Sure.</dd>
                    <dt>Client</dt>	
                        <dd>Will it look good on my boss&rsquo;s Blackberry? And landscape orientation on my iPad2? What about the Surface? I think I might get a Galaxy Tab. Retina screens!</dd>
                    <dt>Me</dt>
                        <dd><img src="FFFFUUUU9000.gif" /></dd>
                </dl>

                <p>A responsive design process is like a scandal. You&rsquo;ve gotta pre-emptively control the conversation. If your client wants to have conversations like this, it likely means you didn&rsquo;t do a good job of setting expectations.</p>

                <p>Design workflow starts in the sales process. Before you land a project, you should be having conversations with your client about what they&rsquo;ll see from you, and when. Most of the projects I&rsquo;m working on right now started that way. Rather than promising multiple rounds of page designs as some of the first design deliverables, we&rsquo;re setting the expectation that the first things they&rsquo;ll see are unstyled HTML to demonstrate content hierarchy and flexibility across various screen sizes and a few pieces of a visual style&mdash;works-in-progress and unrefined broad strokes, not polished visuals that call for critique at a fine detail level. That&rsquo;s why I&rsquo;ve grown to love <a href="/articles/rif-element-collages/">element collages</a>; they&rsquo;re literal enough that your client can start to see a picture of things coming together but still enough of a work-in-progress that there&rsquo;s no expecation that the site would actually look like this for everyone. </p>

                <p>(I don&rsquo;t mean to trivialize the need to communicate some form of visual style to your clients; it&rsquo;s certainly a big challenge, now more than ever. That&rsquo;s why I&rsquo;m really excited about these kinds of conversations and their side effects. <a href="http://twitter.com/jenville">Jennifer Robbins</a> she set out to answer the question, &ldquo;If not Photoshop comps, then what?&rdquo; and <a href="http://www.artifactconf.com/">Artifact Conference</a> was born. I&rsquo;m honored that she asked me to speak there and share some thoughts about what a modern design process looks like. If you&rsquo;re interested in this topic, don&rsquo;t miss this awesome conference where some amazing people will be sharing how-tos about their workflow.)</p> 

                <hr class="sep" />

                <p><a href="http://www.flickr.com/photos/danielmall/8268995579/in/photostream">I&rsquo;m building an office</a> right now. The contractor I&rsquo;ve hired is a wonderful builder, but not really a designer. He can make whatever I ask him to make, but I have to ask him to make it. He also primarily speaks Spanish and only a little English. (A great metaphor for the designer-developer relationship, no?)</p>

                <p>I could create an illustration or a 3D rendering of what I want my new office to look like, but that doesn&rsquo;t take advantage of his great ideas. It&rsquo;s dictation, not collaboration. Instead, I show him <a href="http://pinterest.com/danielmall/studio-ideas/">a Pinterest board</a> my wife and I created. I tell him that I love <a href="http://pinterest.com/pin/112801165638501811/">these beams</a> or <a href="http://pinterest.com/pin/112801165638503115/">this mix of materials</a> and we can have a conversation. I revise my ideas through his expertise, and vice versa. This is how building a website should go. When creating <a href="/work/activate/">the Activate site</a>, I only designed 2 comps; <a href="https://twitter.com/benjaminbojko">Ben Bojko</a>, the excellent developer I was working with, built the rest. A ton of conversation is way more effective than a handful of page designs. Of course, this is built on the assumption of a level of skill among all parties involved and a great amount of trust between them. If you don&rsquo;t have that, stop reading this post and do some research on building great teams.</p>

                <p>To be fair, I don&rsquo;t think we&rsquo;re in a post-PSD era, but I do think we&rsquo;re moving towards a post-&ldquo;full-comp&rdquo; era. I can&rsquo;t envision a project where I don&rsquo;t use Photoshop. Photoshop isn&rsquo;t the problem. It&rsquo;s a great tool. My favorite, actually. It&rsquo;s the stigma that comes with presenting a full comp (I define &ldquo;full comp&rdquo; as an image of a website viewed on a desktop, typically around 960px wide). By default, presenting a full comp says to your client, &ldquo;This is how <em>everyone</em> will see your site.&rdquo; In our multi-device world, we&rsquo;re quickly moving towards, &ldquo;This is how <em>some people</em> will see your site,&rdquo; but we&rsquo;re not doing a great job of communicating that.</p>

                <p>As an industry, we sell websites like paintings. Instead, we should be selling beautiful and easy access to content, agnostic of device, screen size, or context. If you can get your client to believe in the sales process that you&rsquo;ll do that for them, they won&rsquo;t care what the site looks like.</p>
                
