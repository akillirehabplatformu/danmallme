			<h1 class="article-title">Prequalifying Clients</h1>
			
            <p>On our most recent <a href="http://businessologyshow.biz/wordpress/dan-and-jason-answer-listen-questions/">Q&amp;A episode</a> of <a href="http://businessologyshow.biz/">The Businessology Show</a>, a lot of the questions were about onboarding new clients and how to price them appropriately. I figured I&rsquo;d share a few things that SuperFriendly does when meeting a new client.</p>

            <p>Most of our new projects come through email, either cold from one of our sites or through an introduction from a friend. Either way, we&rsquo;ll likely send a fairly generic email to get more information and set up a call to discuss.</p>

            <p>Before I get to the questions, there are two main things that are important here:</p>

            <ol class="disclaimers">
                <li>If a potential client replies back with one word answers, it&rsquo;s a big warning sign that they&rsquo;ll be putting that same kind of minimal effort into a project. We&rsquo;ll still talk to them, but we approach that kind of client much more cautiously.</li>

                <li>The literal answers to these questions are important, but the meaning behind the answers is perhaps even more valuable. What&rsquo;s engrained in the answers is your prospective client&rsquo;s thoughts on how valuable their project is. They&rsquo;re giving you clues on how to price them. More on that in a bit.</li> 
            </ol>

            <p>Here are the topics we like to hit.</p> 

            <section id="timeline" class="topic clearfix">

                <h1>Timeline</h1>

                <h2>&ldquo;What kind of timeframe are we looking at? Is there a certain event or date that is dependent on this site launching? On our end, the soonest availability we have to start this is in _________.&rdquo;</h2>

                <div class="col1">
                    <p>Time is a sign of value. A big part of what your clients pay for is your expertise and the availability for you to put it into practice for them. Clients that want something done right away should be willing to pay accordingly&mdash;read: a lot&mdash;for that service.</p>
                </div><!-- .col1 -->

                <div class="col2">
                    <p>Look out for clients that make up fake deadlines. &ldquo;Our <abbr title="Chief Executive Officer">CEO</abbr> wants it done by May&rdquo; isn&rsquo;t a real deadline, but &ldquo;we want this fantasy football app done two weeks before the season starts&rdquo; is. Bureaucracy certainly exists, but you decide whether you want to work within it.</p>
                </div><!-- .col2 -->

            </section><!-- #timeline -->    

            <section id="existing-work" class="topic clearfix">

                <h1>Existing Work</h1>

                <h2>&ldquo;Is there any work already started that we should know about? A new logo? Some rough designs or ideas for the site?&rdquo;</h2>

                <div class="col1">
                    <p>There are pros and cons to the answer here. Is there work started because the last agency just got fired? Are you picking up sloppy seconds? Finding this out early will help you to make an informed decision about working with the client, but also show you which steps to avoid. Tread lightly though; it&rsquo;s uncomfortable to ask a new sweetheart about past romances, but you also want to know about the likelihood of getting syphilis.</p>
                </div><!-- .col1 -->

                <div class="col2">
                    <p>The answer isn&rsquo;t always bad though. Through this question, we&rsquo;ve often found that the client is already working with an amazing branding, motion graphics, or development shop that we&rsquo;ve been dying to collaborate with. This question surfaces those kinds of opportunities and can make for better holistic work. Get a view of the client&rsquo;s entire ecosystem before proceeding.</p>
                </div><!-- .col2 -->

            </section><!-- #existing-work -->


            <section id="teams" class="topic clearfix">

                <h1>Teams</h1>

                <h2>&ldquo;What size is the project team on your end? Would it involve just you and us? Or would it be us and a whole team on your end?&rdquo;</h2>

                <div class="col1">
                    <p>Team size can tell you a lot about how much an organization values the project they&rsquo;re hiring you for. If the project will commandeer the entire marketing team&rsquo;s time for a whole year, that probably means it&rsquo;s really important to them. However, if it&rsquo;s something they&rsquo;re sticking the new guy on because he has nothing else to do, it&rsquo;s probably not a priority for the organization&mdash;and they&rsquo;re likely not going to be willing to spend a lot either.</p>
                    <p>On the literal end, it&rsquo;s good to know who you&rsquo;re working with. Is it a group of middle managers? What kind of red tape are you dealing with? Even if you&rsquo;re dealing with someone that has decision-making power, will she have <span class="move-along">the availability to respond as quickly as you&rsquo;ll need her to? We&rsquo;ve often had to request certain roles on the client end (like project managers or supporting designers) to make sure our work gets put to good use.</span></p>
                </div><!-- .col1 -->

                <div class="col2">                    
                    <p class="teams-before">You can also get clues about certain stakeholders. Is there a <abbr title="Vice President">VP</abbr> that doesn&rsquo;t care about wireframes but will want to be involved heavily in the aesthetic phase? Does the technology director want to audit your markup at every phase? Use this information to wisely scope and price this project. We don&rsquo;t recommend working with difficult clients, but if you do, don&rsquo;t forget the &ldquo;pain-in-the-butt&rdquo; fee.</p>
                </div><!-- .col2 -->

            </section><!-- #teams -->

            <section id="investment" class="topic clearfix">

                <h1>Investment</h1>

                <h2>&ldquo;How did you hear about our work? What specifically interests you about it? Any particular projects that you're keen on?&rdquo;</h2>

                <div class="col1">
                    <p>This one&rsquo;s a basic weed-out question. We get people who&rsquo;ve never seen our work, thought we were a different agency, mistakenly sent us an inquiry, and lots more.</p>
                    <p>It&rsquo;s important to work with people who respect you and your work. If they don&rsquo;t know you from a hole in the wall, make like a tree and stop wasting your time.</p>
                </div><!-- .col1 -->

                <div class="col2">
                    <p>Keep an eye out for generic terms or buzzwords. They can either let you know that the client hasn&rsquo;t spent much time with your work&mdash;clean! simple! responsive!&mdash;or it can actually reveal what&rsquo;s important to them as an end result.</p>
                    <p>We also want to know if someone referred us, so that we can send them a nice gift!</p>
                </div><!-- .col2 -->

            </section><!-- #investment -->

            <section id="competition" class="topic clearfix">

                <h1>Competition</h1>

                <h2>&ldquo;Are there other people that you&rsquo;re talking to about this project? Might we ask how many? What do you like about their work?&rdquo;</h2>

                <div class="col1">
                    <p>This is often an uncomfortable question to ask, but get used to it. If you can&rsquo;t be upfront and transparent with your clients, you&rsquo;re in for a rough ride anyway.</p>
                    <p>It&rsquo;s nice to know who you&rsquo;re up against. Being informed about your competition isn&rsquo;t just a sales tactic; it helps you to better serve your clients. Lots of studios have a design style; wouldn&rsquo;t it help you to know that your client likes that style, especially if it&rsquo;s not your own?</p>
                </div><!-- .col1 -->

                <div class="col2">
                    <p>Knowing the competitive landscape can also help you better assess your <a href="http://cognition.happycog.com/article/win-some-lose-some">chance of winning</a>. If you&rsquo;re one out of twenty competitors, run! The odds aren&rsquo;t in your favor, or worse: your client doesn&rsquo;t know what they want or how to make decisions. We&rsquo;ve rarely entered situations where there were more than three studios pitching.</p>
                    <p>Working with clients who want to work with you is the best. After all, doesn&rsquo;t everyone want to be wanted?</p>
                </div><!-- .col2 -->

            </section><!-- #competition -->      

            <section id="compensation" class="topic clearfix">

                <h1>Compensation</h1>

                <h2>&ldquo;What kind of budget do you have allocated for this project?&rdquo;</h2>

                <div class="col1">
                    <p>The million dollar question. (Hopefully, literally!)</p>
                    <p>If you&rsquo;re not asking about budget when you first talk to a prospective client, you&rsquo;re doing it wrong. All of these questions are about unearthing <a href="/articles/value-pricing/">the value of a project</a>, but this one is the most direct. Don&rsquo;t skip it.</p>
                    <p>Lots of clients are uncomfortable answering questions about budget. We ask this question specifically knowing that the majority of potential clients will decline to give you a number, both here over email as well as on a follow-up phone call. When you ask, they&rsquo;ll often counter with something like, &ldquo;Well, we want to know how much you think it&rsquo;ll cost.&rdquo; Sometimes, it&rsquo;s because they honestly don&rsquo;t know. Most times though, it&rsquo;s because they have a number in mind and they&rsquo;re hoping you come in lower.</p>
                </div><!-- .col1 -->

                <div class="col2">
                    <p>At this point, we&rsquo;ll use a technique that almost always works for us. Ask them, &ldquo;Is your budget closer to $5K, $50K, or $500K?&rdquo; You&rsquo;re trying to force sticker shock here, so that you get a feel for a budget range.</p>
                    <p>You&rsquo;re looking for a gut reaction. You&rsquo;ll often hear something like, &ldquo;Oh, definitely not $500K.&rdquo; </p>
                    <p>You politely reply, &ldquo;Of course not! How do you feel about spending $50K?&rdquo;</p>
                    <p>They say, &ldquo;Yeah, if we can stick to about $50K, that&rsquo;d feel about right.&rdquo;</p>
                    <p>Voil&agrave;: you&rsquo;ve got a starting point for your project budget.</p>
                </div><!-- .col2 -->

            </section><!-- #compensation -->

            <section id="scope" class="topic clearfix">

                <h1>Scope</h1>

                <h2>&ldquo;What are you specifically looking for from us? Do you already have developers or other partners lined up and are looking to us just for design? Or do you want the whole thing designed, built, and launched?&rdquo;</h2>

                <div class="col1">
                    <p>You&rsquo;ve gotta understand the actual scope of the project. This is the most direct way to do it. This will also answer how much the client knows about you. Are they asking for the thing you do best? Do you even do the thing they&rsquo;re asking for? Is this an opportunity to experiment with services you&rsquo;d like to offer but don&rsquo;t have much experience with?</p>
                    <p>An additional benefit here is that it&rsquo;s a chance to discover what else they&rsquo;re looking for. We&rsquo;ve often heard about dreams of a more coherent social strategy that initially started as a website inquiry. Use this as an opportunity to offer some of your other services&mdash;the <span class="move-along">classic upsell, but done ethically and appropriately&mdash;to make for a more fruitful and prolific relationship.</span></p>
                </div><!-- .col1 -->

                <div class="col2">
                    <p class="scope-before">Tying back to <a href="#timeline">timeline</a> a bit, this answer gives you a chance to gut check you&rsquo;re client too. Are they asking for 6 months&rsquo; worth of work but only giving you 6 weeks to do it? This can show you how much your potential client actually knows about the work they&rsquo;re asking for. It&rsquo;s either a way for you to demonstrate your value and expertise and steer the ship a bit more, or it&rsquo;s a very apparent warning sign that the expectations are misaligned and that you&rsquo;re headed for troubled waters.</p>
                </div><!-- .col2 -->

            </section><!-- #scope -->


            <section id="misc" class="topic clearfix">

                <h1><abbr title="Miscellaneous">Misc</abbr></h1>

                <h2>&ldquo;Anything else that we should know about what you&rsquo;re looking for from us?&rdquo;</h2>

                <div class="col1">
                    <p>The catch all. Time for closing statements. Make sure you don&rsquo;t miss anything. Also, last chance to make sure they&rsquo;re the type of client you&rsquo;d have a meal with. Remember, if you can&rsquo;t hang out, what makes you think you&rsquo;ll be able to work together?</p>                    
                </div><!-- .col1 -->

                <div class="col2">
                    <p>This is your last opportunity in this initial &ldquo;conversation&rdquo; to let your client know that you&rsquo;re listening. Projects where the client feels heard often end in success.</p>
                </div><!-- .col2 -->

            </section><!-- #misc -->

            <p>By now, you should have at least enough info to inform you about whether or not this project will be a good fit&mdash;and so should your prospective client. Don&rsquo;t forget to do your Google-ing and LinkedIn-ing to make sure there isn&rsquo;t some <del>Tumblr</del> <ins>Yahoolr</ins> out there from the last agency that worked with them.</p>

            <p>What did I miss?  Do you ask your clients similar things? Anything to add? Share with the class in the comments!</p>

