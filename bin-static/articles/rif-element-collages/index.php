                <?php /* ?><p>It&rsquo;s easy to do projects the way you&rsquo;ve always done them. Everyone&rsquo;s all crazy about <a href="http://lmgtfy.com/?q=agile">agile</a> lately, but we were all doing just fine with waterfall.</p>
                
                <p>I&rsquo;ve been a long-time fan of planning a site&rsquo;s architecture at the same time as iterating through design. When one precedes the other, there&rsquo;s a strange feeling of being handed instructions&mdash;instructions that you&rsquo;re compelled to break, of course. However, there&rsquo;s a magic that comes with working on the two simultaneous, where they influence each other and they become one and the same.</p><?php */ ?>
                
                <p>For me, design should be tactile. I don&rsquo;t mean that in a <a href="http://lmgtfy.com/?q=skeuomorphism">skeuomorphic</a> kind of way, so don&rsquo;t get your tighty-whiteys in a bunch. I mean it should feel real, like something you can own, something you can be proud of. That&rsquo;s why I&rsquo;ve often opted to show full comps as early as possible; even if there&rsquo;s a lot of iterating to do, I want to paint a picture of the final site that I&rsquo;ll be making for my clients as quickly as I can.</p>
                
                <p>Wireframes and moodboards tend to be too abstract. Perhaps it&rsquo;s just that I&rsquo;ve never perfected the art of articulating their purpose, but I find it akin to showing someone a blueprint of their dream house and asking them what curtains would fit well in the space. There&rsquo;s a huge chasm in being able to visualize the final product from such a vague starting point, but comps are my favorite way to bridge that divide.</p>
                
                <p>However, there are some great designers that I respect tremendously that have had great success with a more abstract form of visual design, so I figured this project would be a great opportunity to try a more effective way to talk about what we would be making together.</p>
                
                <h2>Style Tiles <b style="position: relative; top: -2px;">&rarr;</b> Element Collages</h2>
                
                <p>Ever since <a href="http://badassideas.com/">Samantha Warren</a> <a href="http://www.alistapart.com/articles/style-tiles-and-how-they-work/">unveiled</a> her idea for <a href="http://styletil.es/">Style Tiles</a>, I&rsquo;ve been intrigued about how this can help the design process. I love how Samantha describes the purpose of Style Tiles, with this phrase in particular:</p>
                
                <blockquote>
                    <p>[Style tiles are] a catalyst for discussions around the preferences and goals of the client.</p>
                </blockquote>
                
                <p>In his book, <cite>The Win Without Pitching Manifesto</cite>, Blair Enns describes <a href="http://www.winwithoutpitching.com/presentations-or-conversations">replacing presentations with conversations</a>. When talking about design with my clients, I like to have as much visual fodder as I can, in order to ensure we&rsquo;re talking about the same thing. My version of Style Tiles integrates <a href="/articles/sketchbooks/">my love of collaging</a> for a different type of execution I call an &ldquo;Element Collage.&rdquo; That particular phrase creates an expectation that what we&rsquo;re looking at isn&rsquo;t a final design but rather an assembly of disparate pieces without specific logic or order.</p>
                
                <p>This format represents how I begin to think about designing a site. I often have ideas for pieces of a site in bursts. A full comp often requires ideas to be fully realized. An element collage allows me to document a thought at any state of realization and move on to the next. For <abbr title="Reading Is Fundamental">RIF</abbr>, that element collage allows me to create pieces like this:</p>

                <img src="rif-element-collage-sample.png" alt="An element collage for Reading Is Fundamental" />
                
                <p>You can see the full element collages &mdash;and how they&rsquo;ll eventually merge with the site architecture to form the final site design&mdash;at <a href="http://rif.superfriend.ly/">http://rif.superfriend.ly/</a>. We&rsquo;ll be updating this page as we have more conversations with <abbr>RIF</abbr>, so feel free to check this page regularly.</p>
                
                <p>More soon!</p>
                
