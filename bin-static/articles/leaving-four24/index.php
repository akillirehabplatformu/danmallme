			<h1 class="article-title phark">Leaving Four24</h1>
			
            <p>After just over 10 years, I&rsquo;ve decided to leave my band, <a href="http://four24.com/">Four24</a>.</p>

            <p>In 2002, I played in an emo band. It was either called &ldquo;The Morning After,&rdquo; or that was the only song we wrote and played; I can&rsquo;t remember. Our first show was at the <a href="http://tlaphilly.com/">TLA</a>. I wore leather pants. I didn&rsquo;t have a keyboard of my own to play, so I called my friend Alex to borrow his.</p>

            <p>One day, as I dropped the keyboard off, Alex and I chatted about his band&rsquo;s website. He said their web designer was charging them $500/month for hosting and something obscene to maintain it (of which there were rarely any updates aside from new events). I said he shouldn&rsquo;t be paying more than $10/month for hosting and maintenance should be pretty easy with a simple back-end form (the easy CMS hadn&rsquo;t really been popular yet). He asked if I&rsquo;d be willing to make a new site for them in exchange for a new keyboard of my own. I created this for them, and I owned a new <a href="http://en.wikipedia.org/wiki/Korg_Triton">Korg Triton LE</a>.</p>

            <figure>
                <img src="site.png" alt="The first site I designed for Four24" />
                <figcaption>The first site I designed for Four24</figcaption>
            </figure>

            <p>Seeing that I had my own keyboard now, Alex asked if I wanted to join their band. I was friends with most of the guys in the band already, as we had played music together in church for years. These guys were incredible musicians and vocalists; think about the "popness" of <a href="http://www.boyziimen.com/">Boyz II Men</a> but with more intricate harmonies like <a href="http://www.take6.com/">Take 6</a> and a really groovy <a href="http://theroots.com/">Roots</a>-style band in the background, all with a bit of Latin-flavor and with a Christian message focused on ministry. I couldn&rsquo;t join fast enough.</p>

            <p>My first event was horrible. The sound was so bad that I couldn&rsquo;t hear anything. At the end of the first song, I realized I just played the whole thing in Gm, but the song was in Dm. Whoops.</p>

            <p>In the last 10 years, I&rsquo;ve learned <em>so</em> many things from these guys. When I started, I didn&rsquo;t even know what a bass-line sounded like. Now I can understand any part in a 5-part harmony, arrange parts for multiple instruments and vocals, play multiple styles of music, and so much more. I still can&rsquo;t play salsa though.</p> 

            <p>My favorite part has been how much this band has been a ministry, not just to the people who listen and buy our CDs, but to the guys in the band as well. We&rsquo;ve brought music and food to the homeless, seen people possessed by demons, watched drug addicts give their lives to Christ, cleaned city parks, traveled across the country, and so much more. Former band members have passed away, had kids, gotten married, gotten divorced, left the band, returned to the band, and everything else that life brings.</p> 
            
            <p>Through the years, I&rsquo;ve had <a href="/work/four24/">plenty of chances</a> to lend my design talents to further the cause. Here are a few of my favorites:</p>
            
