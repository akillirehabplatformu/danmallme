            <p>When I joined, I was a kid in college with a girlfriend. Now I&rsquo;m married with a kid and my own business.  There&rsquo;s a 21-year gap between the oldest and youngest member. I can&rsquo;t make the commitment anymore that I originally signed up with, and we all agreed that it&rsquo;s best the band move on without me. I love these guys; they are my brothers. Not to mention, my own actual <a href="http://twitter.com/jahwnie">brother</a> is also in the band (and is a filthy-good guitarist). I wholly believe in Four24&rsquo;s mission and what they&rsquo;re doing to reach souls.</p>

