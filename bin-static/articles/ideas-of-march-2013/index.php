                    <h2>A response to the #ideasofmarch</h2>
                    <h1>The lack of blogging about the lack of blogging</h1>
                </hgroup>

                <p>Every year, my friend <a href="http://shiflett.org/">Chris Shiflett</a> writes about an idea called the &ldquo;<a href="http://shiflett.org/blog/2013/mar/ideas-of-march">Ideas of March</a>.&rdquo; It&rsquo;s a call to arms for more blogging as a way to continue spreading great ideas. This year, he&rsquo;s asked that I participate. Ironically, it&rsquo;s taken me until the last day of March to actually get around to it.</p>

                <p>I love blogging. I&rsquo;ve been doing it for the last 9 years. When I launched this version of my site, I specifically designed it in a way that allows me to more effectively tie new content with presentation. I&rsquo;ve been really proud of the results, specifically with posts like &ldquo;<a href="/articles/you-the-conference-organizer/">You, the conference organizer</a>&rdquo; and &ldquo;<a href="/articles/superfriendly-annual-report-2012/">The 2012 SuperFriendly Annual Report</a>.&rdquo;</p>

                <p>However, there are a lot of reasons I don&rsquo;t blog as often as I&rsquo;d like to. Things that get in the way: time, client work, lack of desire, the insecurity that I don&rsquo;t have anything valuable to say, and lost more. I&rsquo;m sure this isn&rsquo;t exclusive to me.</p>

                <p>I use <a href="http://notational.net/">Notational Velocity</a> to keep track of all my ideas. One of those particular notes is a list I&rsquo;ve been keeping for a few years when a potential blog topic pops into my head. As a complete cop-out, I&rsquo;m publishing my list of topics, unfiltered and unedited. Some are ridiculous, others are outdated or already meticulously covered elsewhere, and I&rsquo;m excitedly hoping to get to the rest someday.</p>

