                    <p><abbr title="General Electric">GE</abbr> approached Big Spaceship with an unusual question: how can we get people to think about their health in a more positive way? We brainstormed for months about the possibilities, and came to a few important realizations: </p>

                    <ol>
                        <li>We only think about their health when things go wrong, like when we get hurt or need an emergency procedure. This makes us view health as a negative topic.</li>
                        <li>We want to make healthy choices, but it&rsquo;s often difficult to do so.</li>
                    </ol>

                    <p>To that end, our task was to create ways that <abbr>GE</abbr> could empower consumers to be healthier, and feel good about it. Coinciding with the 2010 Winter Olympics, we created a number of platforms and partnerships, including an app called <a href="/work/morsel/">Morsel</a>, the <a href="http://www.webmd.com/a-to-z-guides/health-a-z-tools/better-health-conversation/default.htm">Better Health Evaluator with WebMD</a>, a <a href="http://info.howcast.com/physicalchallenge">video series</a> with <a href="http://www.howcast.com/">Howcast</a>, and a media buy called <a href="http://www.healthymagination.com/sharingideas/">Sharing Healthy Ideas</a>, among other things.</p>

                    <p>Healthymagination.com became the hub that housed these initiatives and gave people a centralized way to view the whole ecosystem.</p>

