                    <p>Prior to the release of <a href="http://archive.bigspaceship.com/warforcybertron/site/">Transformers: War for Cybertron</a>, Activation asked Big Spaceship to pitch a few concepts for a teaser site. Before starting, Michael Lebowitz gave me this excellent creative direction: if Transformers are the ultimate sophisticated machines from space, their <acronym title="Heads Up Display">HUD</acronym> wouldn&rsquo;t look like they were created in the 90&rsquo;s. Kill the traditional 45&deg; angles and pixel fonts; what would this thing look like if they had hired their planet&rsquo;s equivalent of Apple or Nintendo to design their displays?</p>

                    <p>Excited by the possibilities, I was off to the races. Conceptually, our premise was that you were playing as a Transformer (whether you were an Autobot or a Decepticon was still a secret at this point) who would been blown clear of the planet in the heat of war. As you regain consciousness, you try to piece together memories of what happened by exploring pieces of debris and other artifacts in space using your <acronym>HUD</acronym>&rsquo;s advanced controls and design.</p>

                    <p>Ultimately not the chosen direction, I&rsquo;m very pleased with how this turned out. Autobots roll out!</p>

