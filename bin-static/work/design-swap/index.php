            <div class="main">

                <p>Just after <abbr title="South by Southwest">SXSW</abbr> 2010, <a href="http://yaronschoen.com/">Yaron Schoen</a> and <a href="http://trentwalton.com/">Trent Walton</a> started a little side project called <a href="http://design-swap.com/">Design Swap</a> to facilitate collaboration between designers. I was fortunate enough to be invited to participate as the next installment.</p> 

                <p>When asked if I had a particular person I&rsquo;d like to pair up with, I mentioned <a href="http://behoff.net/">Brian Hoff</a> because I&rsquo;d never met him. Yaron mentioned that Brian chose me too, so it seemed the stars were aligned. Since Brian and I didn&rsquo;t know each other aside from a few tweets and emails, we decided to use the opportunity to change that. After a few phone calls to brainstorm, we arrived at the idea of compulsively tracking everything we did for a full day, then giving the other carte blanche to do whatever he saw fit with the data. The only restriction was that the execution must look like it was designed by the other person.</p>

                <p>Using the design direction of Brian&rsquo;s site&mdash;<a href="http://www.thedesigncubicle.com/">The Design Cubicle</a>&mdash;I presented his day in 3 views: a random view that displayed one illustrated and animated portion of Brian's day, a chronological view that organized his daily activities against an analog clock (made entirely with CSS!), and a statistical view that charted objects from Brian&rsquo;s day.</p>

                <p>All in all, I consider the exercise a huge success. It was my first opportunity to get my feet wet with HTML5, and I made a great friend in Brian through the process. (Cue sappy music&hellip;)</p>

