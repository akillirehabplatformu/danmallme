                <div class="main">
                    
                    <p>Microsoft challenged <a href="http://bigspaceship.com/">Big Spaceship</a> to take on the beta release of <abbr title="Internet Explorer">IE</abbr>9&mdash;to explore the new features and functionality of the browser and push the limits of what&rsquo;s possible. We responded with &ldquo;<a href="http://always-beautiful.bigspaceship.com/">Always Beautiful</a>&rdquo;, an interactive experience that uses the history of the web as a personal paintbrush.</p>

                    <p><abbr>IE</abbr>9&rsquo;s new support for web standards and <abbr title="Scalable Vector Graphics">SVG</abbr> are impressive, but hard to comprehend in the abstract. We wanted to show how these new capabilities translate into unparalleled opportunities for users and developers.</p>

                    <p>Drawing inspiration from the ever-evolving trends of the Web and setting them against a soundtrack from recording artist <a href="http://www.mingmusik.com/">Ming</a>, users generate a completely unique piece of artwork from a library of Web-inspired graphic assets we created. <abbr>IE</abbr>9&rsquo;s unique use of the user&rsquo;s graphics processor allowed for hundreds of assets on the screen at once with no drop in performance. And with native browser support of <abbr title="HyperText Markupt Language">HTML</abbr>5 standards and <abbr>SVG</abbr>, the creation could be be generated based on real-time user input and output at any size.</p>

                    <p>The result? A beautiful piece of artwork rich in detail, representing the beauty of the Web in a way only <abbr>IE</abbr>9 can deliver.</p>
                    
