                    <p>Every once in a while, you get to be involved in something amazing.</p>

                    <p>I&rsquo;ve had the wonderful opportunity to art direct a website redesign project for <a href="http://housingworks.org/">Housing Works</a>, an inspiring organization that provides houses for homeless people living with <abbr title="Human Immonudeficiency Virus">HIV</abbr> and <acronym title="Acquired Immunodeficiency Syndrome">AIDS</acronym>.</p>

                    <p>It&rsquo;s not every day that a project like this comes along. Drama in the life of a web designer consists of chasing typefaces and perfecting color schemes, crying when our applications crash, and stressing about deadlines. For Housing Works, their services are sometimes the factor between life or death. Talk about perspective.</p>

                    <p>That might seem like a lot of pressure; for me, it was more like reference. It became less about how well I could design and, instead, more about any little way that I could make it easier for Housing Works to eliminate <acronym>AIDS</acronym> and homelessness.</p>

                    <p>There&rsquo;s a lot involved in designing a site like this. One of the biggest truths to accept is that you can&rsquo;t just hope to work from your desk. Doing sketches and opening Photoshop without any frame of reference would have been an insult. I was grateful to see with my own eyes why this was so important.</p>

                    <p>Even before starting to think about design, I took a trip (with <a href="http://jasonsantamaria.com/">Jason</a> and <a href="http://heathershaw.com/">heather</a>, the other <a href="http://happycog.com/">Happy Cog</a> designers) to witness Housing Works in action first-hand. We took a trip to the <a href="http://www.housingworks.org/services/housing/programs/">Keith D. Cylar House</a> and sat in on a community meeting. The residents and the staff talked about arranging social events, ways to improve living situations for others in their shoes, and organizing trips to lobby Congress into passing a bill in support for funding for aids. We visited some of Housing Works&rsquo;s <a href="http://www.housingworks.org/social-enterprise/">social enterprises</a>, like the <a href="http://www.shophousingworks.com/locationDetail.cfm?storeID=7&entry=416&section=bookstore">bookstore caf&eacute;</a> and some of their <a href="http://www.shophousingworks.com/locations.cfm?from=sidebar&section=thrifts">thrift shops</a>. Exposure as ammo, we set out to brainstorm.</p>

                    <p>With all of the range that the organization covers, it was hard to choose a single concept. At the end of it though, it was very clear that the heart of Housing Works is the people that serve and people that it serves. It was overwhelmingly about warmth and care. Under that direction, it was easy to design the site you see today.</p>

                    <p>Many thanks and admiration go to the Housing Works team&mdash;specifically Chris Sealey, Ian Crowther, and Keith Mancuso&mdash;for all you do in bettering the world.</p>

