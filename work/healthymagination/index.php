                    <p>This required a dramatic shift toward engaging people in an ongoing dialog around better health. Orchestrating a team of partners, we defined the strategic direction, guiding all efforts in creating a broad ecosystem.</p> 

                    <p><a href="http://healthymagination.com/">Healthymagination.com</a> acts as the hub of that ecosystem, with media-rich features, a portfolio of projects, and the new healthymagination blog. We took the lead on <abbr title="User Experience">UX</abbr>, content strategy, and design in launching the site to coincide with <abbr>GE</abbr>&rsquo;s promotional campaign centered around the 2010 Winter Olympics.</p>

