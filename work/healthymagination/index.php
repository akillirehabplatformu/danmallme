                    <p>Healthymagination encompasses a variety of ongoing and evolving projects, which we launched simultaneously, including:</p>

                    <ul>
                        <li><a href="http://www.webmd.com/a-to-z-guides/health-a-z-tools/better-health-conversation/default.htm">The Better Health Evaluator</a>, a partnership with <a href="http://webmd.com/">WebMD</a>, is transforming the patient-doctor relationship. It&rsquo;s a targeted dialog that helps people craft a custom set of talking points and questions so they can get more out of their next doctor&rsquo;s visit.</li>
                        <li><a href="/work/morsel/">Morsel</a>, a mobile app that makes healthier decisions a part of everyday life. It offers simple, fun, daily tasks that help individuals get healthier one step at a time. Over 20,000 Morsels were completed with the first two weeks of launch, and enhancements are on the way.</li>
                        <li><a href="http://www.healthymagination.com/sharingideas/">Sharing Healthy Ideas</a>, a media partnership that makes it easy to find a snapshot of the most talked about issues in health. When people share an article on any of the partner sites, that activity is aggregated into data visualizations that provide a novel way to explore and navigate health content.</li>
                    </ul>

